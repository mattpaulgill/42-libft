#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	unsigned char	*a;
	size_t			i;

	a = str;
	i = 0;
	while (i < n)
	{
		a[i] = c;
		i++;
	}
	return (a);
}
