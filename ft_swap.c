#include "libft.h"

void	ft_swap(void *a, void *b, size_t s)
{
	void	*x;

	x = malloc(s);
	ft_memcpy(x, a, s);
	ft_memcpy(a, b, s);
	ft_memcpy(b, x, s);
	free(x);
}
