#include "libft.h"

char	*ft_strrev(char *s1)
{
	int	i;
	int	j;
	int	len;

	i = 0;
	len = ft_strlen(s1);
	j = (len - 1);
	while (i < j)
	{
		ft_swap(&s1[i], &s1[j], sizeof(char));
		i++;
		j--;
	}
	return (s1);
}
